  <h3>Mr. and Mrs. Frugal Hacker from <a href="https://www.frugalhackers.com/" target="_blank">Frugal Hackers</a></h3>
  <p class="lead">27f/30m, married and saving in San Francisco, CA. $480k+ in savings. Spending $56,845.07/yr now, $36,568.63/yr in retirement. FIRE 30 (Mrs) and 33 (Mr) in a super expensive city.</p>
  <p>
    We are two young married Canadians of Indian origin living and working in San Francisco making $300k in annual income before taxes and expenses. Mr. Frugal Hacker is a Software Engineer and Mrs. Frugal Hacker is a CPA. A typical weekend is usually some combination of long-distance cycling, hiking, walking around San Francisco, board games with friends, building personal finance spreadsheets and working on our blog, frugalhackers.com
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>Paul Graham <a href="https://www.frugalhackers.com/blog/2017/6/24/tweet-discovering-financial-independence" target="_blank">retweeted a blog post</a> by Mr. Money Mustache. Followed that link to MMM's blog, and we had our aha! moment.</p>

    <h4>What was your financial status when you began?</h4>
    <p>It was June 2016, and we had just bought a 3BR/2BA condo apartment in San Francisco. We didn't have any debt other than the mortgage and had ~$200k in net worth at the time.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>We started investing all of our savings in index funds. We've always been good at frugality and saving money. However, we didn't start investing in the stock market until we turned 26 and 29 (excluding some 401k investments).</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>We wish we started investing in the stock market earlier in our careers. We also wish we had maxed out our 401k contributions every single year since we started working. We had the savings, but simply didn't take advantage of our employers' 401k plans.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Pay attention to your savings rate. It's the single most important metric that determines how quickly you can reach FI. </p>
