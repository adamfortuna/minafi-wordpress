  <h3>Jim from <a href="https://www.routetoretire.com/" target="_blank">Route to Retire</a></h3>
  <p class="lead">41m, married, working and saving money in Ohio. ~$1.08m in savings. Spending $60k/yr now, $45k in retirement. FI &amp; RE by 49 in a rural area.</p>
  <p>
    I'm currently in IT management and have been at the same company for over 18 years.  While they've always taken care of me, I don't enjoy the day-to-day anymore.  When my daughter was born, I hated having to go to work every day - I felt like I was missing out even though this is common for dads to do.  Since then, I've been on a mission to be free from the financial burden shackling me to an office every day.  We now have three rental units and are hoping to purchase 2-3 more before I quit my job in a few years.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>My big wake-up call to FI was reading Robert Kiyosaki's books ("Rich Dad Poor Dad" and "Cashflow Quadrant") years ago.  It wasn't that they were step-by-step guides, but more of a realization that I don't have to be stuck in the rat race forever.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I had just finished working hard to get myself out of around $35k of credit card debt.  I had a clean slate and was ready to make things happen.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Changing the funds in my 401(k) from managed funds to a low-cost Vanguard index fund is likely to save me over $60,000 in fees just over the next 8-9 years.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I wish I had started earlier.  If I had house-hacked or started in real estate years earlier, I would be much further ahead than I am today.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Start early, find a high-paying job you enjoy, live a very modest lifestyle, and save and invest a very large percentage of your income.</p>
