  <h3>Mrs. Adventure Rich from <a href="https://www.adventurerich.com/" target="_blank">Adventure</a></h3>
  <p class="lead">27f, married and working and saving money in Michigan. $285k in savings. Spending $55k/yr now, $35k in retirement. FI by ~45 in a ruralish area. RE TBD on what life throws at us!</p>
  <p>
    I am a wife, mother, and outdoor lover who has a hard time sitting still.  My husband, son and I live in our dream location, beautiful northern Michigan, in what we hope is our "forever home". We are adventurers disguised as a business professional for a Fortune 40 company (me) and a facilities manager (him).
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I first learned about FI through Mr. Money Mustache's blog. From there, I found blogger after blogger sharing their FI journey and goals.  My husband and I were hooked!  </p>

    <h4>What was your financial status when you began?</h4>
    <p>When Mr. Adventure Rich and I first found out about early retirement, we were about to get married and carried around $50K+ of car loan, student loan and credit card debt.  We buckled down, paid off the $50K and started stacking our money for two big goals, our house and our retirement.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>I would say there have been two key changes we made as we pursued FI.</p>
    <p>One was a relentless effort to stash as much money as possible outside of our checking account.  Our retirement contributions come straight from our paycheck and we direct deposit a portion of our pay into savings accounts as well.  The automatic savings has helped us save for our goals (retirement, emergency fund, house fund, etc.) because we never have to think about it or are tempted to touch it!</p>
    <p>The second change was an overall mindfulness about our spending.  Do I really need the extra box of snacks at the store?  Or that $50 item on Amazon?  Will I really be happy with that $5,000 vacation?  It has been a process of understanding that yes, we can afford to purchase random luxuries or spend money on a plethora of things, but we choose not to.  Instead, we choose to buy our time... our freedom and retirement.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>Personally, I would have not bought a new car out of college.  It wasn't too bad (~$18k for a new Nissan Versa Hatchback which I sold 5 years later for $6k with 90K miles on it), but it was a depreciating asset.</p>

    <p>I also would have saved more earlier.  I always took advantage of my employer match, but I wish I had just jumped into maxing out my 401K Year 1 of work and not waited until Year 4.  Granted, in that time, I paid off my car, my student loans and paid for a good chunk of our wedding (my parents pitched in too), but still... I think I could have found ways to ramp up my savings rate earlier.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Stick with it, even in the ""dry spells"" or hard times when goals seem far off or thwarted by unexpected expenses.  Find the joy and adventure life has to offer every step of the way.  Go outside, take a walk, watch the sunset, read a good book... just go live!</p>
    <p>Oh- and try not to get into the comparison game (both for the spenders and the savers!)  There will always be people who have more money and/or spend more money, ignore it.  There will also always be people who save more and reach FI/RE earlier... learn from them and use their stories as motivation!  Then, go on and live your own life :)</p>
