  <h3>Jon Dulin from <a href="http://www.moneysmartguides.com" target="_blank">MoneySmartGuides</a></h3>
  <p class="lead">38m, married and saving in Philadelphia, PA. $700k in savings. Spending $75k/yr now, $60k/yr in retirement. FIRE at 55 in a city.</p>
  <p>
    My name is Jon and I live outside Philadelphia with my wife and our daughter. Both my wife and I hate debt and want to quit the rat race as soon as possible. As we are raising kids right now, we have to balance our spending and savings and this is a monthly task. Some months are better than others, but through it all, we keep our focus on our goals and save as much as we absolutely can. Currently we are on track to retire and be FI by 55.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>After college, I was talking with a friend about how we wanted our lives to unfold. After talking, I realized that I would never be able to achieve my dreams just by working a 9-5 job. I needed to create a plan and start taking action. During this time I stumbled onto Early Retirement Extreme and the rest is history.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I had a mountain of credit card debt that I had to dig myself out of. But even when I was doing that, I was investing small amounts of money in my 401k and in a taxable account.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Questioning spending. Instead of getting caught up in the emotions of buying something, I now stop and think before making any purchase. It has helped tremendously to avoid purchases I later regret.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I wish I didn't have the credit card debt when I started. I'd be much farther ahead of the game. But, I learned a lot about debt and money by being in debt, so even though I wish I never had the debt, it has made me smarter with money now.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Start now. Even if you can only save a few dollars or pay a little extra on your debt. Every little bit helps and grows and compounds in years to come. So take the first step. By taking the first step, you have a chance of FI. If you never take that first step, it will never happen.</p>
