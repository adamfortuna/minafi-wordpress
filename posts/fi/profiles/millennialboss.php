  <h3>J. from <a href="http://millennialboss.com" target="_blank">Millennial Boss</a></h3>
  <p class="lead">27f, married, working and saving money in the Pacific Northwest. $300k in savings. Spending varies by location, $60k in retirement. FI by 35. RE by 40 in an city metro.</p>
  <p>
    I'm J and I love dabbling in various side hustles, travel hacking, and reading personal development books. I plan to retire with my husband and my dog near family but the truth is that I love dabbling so much that I always see myself bringing in some sort of income. I'm currently an Etsy seller and make a few hundred dollars per month through that.
  </p>

  <div class="profile-toggle--more">
    <p>Call the Early Retirement Police I dare you. I'm also an early retirement podcast co-host, former collegiate athlete and wannabe current athlete but I get so swept up in my side hustles that I don't have time to workout. I go to FinCon, Chautauqua and all the FIRE conferences and meetups because I love this community.</p>


    <h4>How did you learn about FI?</h4>
    <p>I found Mr Money Mustache through the blog No More Harvard Debt which encouraged me to pay off nearly $100k in student loans in 18 months.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I was nearly $100k in debt when you consider my husband's student loan debt and my car loan/credit cards.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>I took career hacking to the extreme in the last 5 years and increased my income exponentially. I also switched into the tech field which helped with that. I love my job now. The biggest challenge is cutting back on housing in a high cost of living area.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I believe the Rascal Flats lyrics "God Bless the Broken Road That Led Me Straight to You," and I truly wouldn't change a thing about my life - both the good and bad. It led me to where I am today. I'm one of those people who thinks that something as small as a sneeze could change the path of your life. I wouldn't go back in time and change anything out of fear it wouldn't bring me here.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>I have two pieces of advice. First, the first step is the scariest. My husband and I sold our home, new car, all of our stuff and left our friends and family to chase career opportunities out West. It was hard but that first step is the hardest. It gets easier once the debt is gone and you start accumulating. Second, meet other FI-ers in person as it affirms your path.</p>
