  <h3>Early Retirement Dude from <a href="https://earlyretirementdude.com" target="_blank">earlyretirementdude.com</a></h3>
  <p class="lead">48m, married and retired in the Southeastern US. $2.3m in savings. Spending $55k/yr. FI & RE at 36 in a ruralish area.</p>
  <p>
    I'm a Deadhead-type GenX'er. Love running and bicycle touring. Averse to the toxicity of consumer culture and the modern workplace. Trying to wake people up to the idea that financial independence/early retirement A) exists, B) can be an antidote to that toxicity, and C) is actually achievable.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>Honestly, I came up with my own version of it in a vacuum. I graduated from MBA school and went corporate in 1993, so I didn't have access to the web-based resources available today. Found myself immediately miserable. At the time defined-benefit pensions were being killed off by 401(k) plans, so between the financial modeling tools I'd learned in school and the idea that I'd have to fund my own retirement, I cooked up a plan to get out of the workforce much earlier than 57 1/2.</p>

    <h4>What was your financial status when you began?</h4>
    <p>Started with a few hundred bucks and a beat-up car and a couch from Goodwill that was covered in suspicious purple stains.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>When I was twenty-six I got passed over for a great job because I'd stirred up some political problems through sheer arrogance. I really wanted that job, so I called up the hiring managers and asked them if we could get together for a feedback session. What could I learn/do differently/etc. to earn an offer during the next round of hiring? Asked them that in all humility. They gave me straightforward feedback, I got the training and made the changes they recommended, and ended up getting hired. If I hadn't asked for that meeting I'm convinced I'd still be working.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I wish I hadn't put so much pressure on my wife to be frugal. Twenty years into our marriage, now, and she still feels guilty when she spends money.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Understand that it's not just a numbers game. If you focus on FI/ER to the exclusion of all else, you can wreck things that are very important to you--relationships, pastimes, creative outlets, etc. It's a serious commitment. Know what you're getting into.</p>
