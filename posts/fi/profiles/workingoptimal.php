  <h3>Working Optional from <a href="https://www.workingoptional.com" target="_blank">Working Optional</a></h3>
  <p class="lead">40~45, married and living in Southern California. Half way to FI. Spending $90k/yr now, $60k in retirement. FIRE at 53, RE in 50s in a city metro.</p>
  <p>
    Self-employed, work in IT, newbie Personal Finance blogger. Looking to increase semi-passive income via real estate, informational products, SaaS apps etc.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>Came across a few blogs while browsing Bogleheads, Reddit and then it took off from there. Having a minor mid-life crisis helped.</p>

    <h4>What was your financial status when you began?</h4>
    <p>Arrived in the US with a $2,000 loan ~18 yrs ago. Worked full-time, got an MBA from a top 15 school part-time. Paid off all student loans, got married, had kids, got a mortgage (twice) etc over the next decade. The last ~7 years have had a nice positive impact on my finances.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Earlier: Becoming self-employed. There are highs and lows, but it has taught me to be naturally frugal and "save for a rainy day". The highs jumpstart my savings.</p>
    <p>Recently: Changing my mindset i.e. deciding to pursue FI and realizing that it *was* possible, and that it was what I wanted but didn't know how to describe it.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>Could have been smarter about some investments made in 2010-11 and should have jumped on the real estate bandwagon as well.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Save &amp; Invest - the two items that you can control even if you can't switch jobs/careers. Live a modest lifestyle.</p>
