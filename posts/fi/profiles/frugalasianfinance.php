  <h3>Ms. FAF from <a href="https://frugalasianfinance.com/" target="_blank">Frugal Asian Finance</a></h3>
  <p class="lead">30f, married, working and saving money in Washington, D.C. $320k in savings. Spending $50k/yr now and in retirement. FI &amp; RE by 50 in an city metro.</p>
  <p>
    I am an Asian personal finance blogger, a full-time working mother of one, a devoted wife, and a real estate dreamer. I am passionate about saving money while living a fulfilling life.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I learned about FI after joining the personal finance blogger community on March 21, 2017. </p>

    <h4>What was your financial status when you began?</h4>
    <p>My only debt was the mortgage on our house, which we’re aggressively trying to pay off. My husband and I have always been frugal. We’ve been conscious about saving and investing whether we’re trying to achieve FI or not. </p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Dedicating myself to my blog (FrugalAsianFinance.com) to show myself and others that I am serious about financial responsibility. </p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I wish I could have started my blog sooner to join the amazing blogosphere earlier. </p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Saving money and investing is great, but don’t forget to enjoy life. </p>
