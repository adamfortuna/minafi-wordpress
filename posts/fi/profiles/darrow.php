  <h3>Darrow from <a href="http://www.caniretireyet.com/" target="_blank">Can I Retire Yet?</a></h3>
  <p class="lead">57m, married and retired in Santa Fe, NM. $1m+ in savings. Spending <a href="http://www.caniretireyet.com/our-retirement-expenses-money-go/" target="_blank">varies</a>. FIRE at 50 in a city metro.</p>
  <p>
    Early-retired civil and software engineer, author, investor who loves outdoor sports like climbing, biking, and hiking.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>By pursuing my dream of freedom from a 9-5 job.</p>

    <h4>What was your financial status when you began?</h4>
    <p>Frugal, relatively high-earning engineer.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Not inflating lifestyle (houses and cars) as I received raises and bonuses in my career.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>Hindsight is always 20/20. In reality I think I made the best decisions I could to reach the goals I had at the time.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Spend in the few areas that matter to you. Cut back everywhere else. Avoid big houses and cars. Learn to invest prudently for the long haul.</p>
