  <h3>J. Money from <a href="http://budgetsaresexy.com" target="_blank">Budgets Are Sexy</a> and <a href="http://rockstarfinance.com" target="_blank">Rockstar Finance</a></h3>
  <p class="lead">37m, married, working and saving money in Washington, D.C. $657,837.01 in savings. Spending $72k/yr now, $48k in retirement. FI by 45 in an expensive city. RE never. :)</p>
  <p>
    I'm a daddy of two, blogger of two, and been a self-employed blogger since 2010. Love to see all the new faces coming up in the community as we need as much help with this $$$ stuff as we can get out there.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>Oh man, from the original OG of the FIRE movement before it was even a "thing" - Jacob Lund Fisker of EarlyRetirementExtreme.com! Although I sorta thought he was nuts until Mr. Money Mustache came around and blew it up even more ;) From that point forward I started actually applying myself and it's been an interesting 5+ years ever since. Super thankful for them all.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I had about $50,000 of net worth when I came onto the blogging scene back in February of 2008, but really wasn't that great - nor horrible - with money then. I was kinda so-so and going with the flow until we bought our house at the peak of the market with no money down and 100% financing, and that was the wake-up call to start paying attention more and get on my $hit. I stumbled across the financial blogging world from there and it completely transformed both my wallet and my mindset. Not to mention my friends and career and outlook and everything. Huge game changer for me.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Probably that I went from chasing millions just to be able to say that I'm a millionaire, to chasing "enough" just so that I can be financially FREE. That was the biggest change of everything for me over the years. Realizing that it's not about the money at all, but how it can be used to set yourself up for a most beautiful lifestyle.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I would have stopped working on 10 billion blogs and projects at the same time, and instead focused on only one or two and just really KILLED those to have a more relaxed lifestyle from the start. The more I've been in this blogging/career world, the more I realize that pouring your efforts into one main slot can powercharge your goals vs spread out over time.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Remember that it's not about the money!! And to try and enjoy your lifestyle <b>today</b> vs always being obsessed with the <b>future</b>. A lot of this stuff requires TIME in order to have your $$$ marinating and growing, so you have a lot of living to do before that magical moment of hitting your "number." Soak it up!</p>
