  <h3>Gwen from <a href="http://www.fierymillennials.com/" target="_blank">Fiery Millennials</a></h3>
  <p class="lead">26f, single, working and saving money in the Midwest. $170,000 in savings. Spending $40k/yr now, $25k in retirement. FI by 28 in a ruralish area. RE by 30.</p>
  <p>
    Hey, I'm Gwen! I'm a 26 year old IT professional on my way to early retirement. I enjoy playing sports, cuddling my cat, reading and working on my real estate properties.

  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I found <a href="http://www.mrmoneymustache.com/" target="_blank">Mr. Money Mustache's blog</a> my junior year of college and was immediately hooked. I spent the rest of my time in college soaking up information from blogs, prepping, and strategizing.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I graduated from college with $10,000 to my name, a paid off car, and no debt at the age of 23.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Not allowing lifestyle inflation to creep in. Keeping my spending low has been an enjoyable challenge.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I wish I had gone for slightly cheaper housing in 2016. I spent a lot of money to live close to work. It would've been better for me to live slightly further away, but I wanted to bike to work.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Keep your expenses low and focus on growing your income as much as you can. You can only cut your expenses so much, but the sky is the limit when it comes to earning more money.</p>
