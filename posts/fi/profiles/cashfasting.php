  <h3>Jane from <a href="https://cash-fasting.com" target="_blank">Cash Fasting</a></h3>
  <p class="lead">25f, working and saving money in New York City. $65k in savings. Spending $25k/yr now. FI by 35. RE TBD in a city metro.</p>
  <p>
    I'm a VA native trying to build wealth in the Big Apple. I'm relatively new to the idea of FIRE, but I've jumped on board and I'm eagerly tracking my progress. I enjoy dancing, cooking, trying out new restaurants, and telling my friends to invest more in their retirement accounts.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I've always had an interest in personal finance. In my first job out of school, I did market research for credit card companies, and was the go-to gal in my friend group for the knowing the best credit card offers, terms, and bonuses. In 2015, my boyfriend introduced me to Mr. Money Mustache. That sparked the idea of FI for me; that I could take all this PF knowledge that I was accruing and apply it to my future in a way that wasn't just building the biggest nest egg possible.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I finished graduate school $35K in debt. I don't think I had more than $2K in my checking account, but let's be real, that was my parents' money. Luckily I didn't have to pay for any large expenses like a car and I lived at home straight after graduation, which allowed me to focus on my debt right away.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Increasing my income. I'm not the most frugal person (though I try to be), but you can only decrease your expenses by so much before it stops making sense to do so. At the beginning of 2017, I negotiated a 20% increase in salary, which helped me <a href="https://cash-fasting.com/how-i-paid-off-35k-of-student-debt-in-3-years/" target="_blank">pay off my debt</a> earlier than expected.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>Lived at home longer. After a year and a half at home, I moved out to live with a couple of friends. It made my commute longer, and increased my expenses by a lot. I definitely enjoyed the experience, but I wasn't unhappy at home. The nine months I lived with friends before moving to NYC probably cost me a good $10K.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Set short-term goals. Pursing FI is more of a lifestyle change than it is pursing a long-term goal. Ask around; people who have reached their FI number don't have an accompanying revelation or giant increase in happiness. If you set smaller goals like "pay off loans by 2018" or "save $5K by the end of the year", the boost you get from reaching those goals will help you stay focused on the bigger one. Also, don't forget to enjoy the journey.</p>
