  <h3>Amy Blacklock from <a href="https://lifezemplified.com" target="_blank">Life Zemplified</a></h3>
  <p class="lead">49f, married, working and saving money in Michigan. $900k in savings. Spending $75k/yr, $50k in retirement. FI by 53. RE by 55 in an rural area.</p>
  <p>
    My current day job is in the automotive industry as a project analyst with additional responsibilities in office management. On the side I'm a nutrition and health coach, blogger, and freelance writer. I enjoy a variety of sports and fitness activities, and I love learning and trying new things.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I stumbled upon Mr. Money Mustache at the age of 45 and realized I wasted a lot of money and time.</p>

    <h4>What was your financial status when you began?</h4>
    <p>I was newly married after being divorced for a couple years. We both had some money in 401k plans and IRA's but nothing to brag about. I think we were both saving around 10% in our 401s at the time. We had some debt, car loans and a mortgage, but no plan to eliminate it other than making the monthly payments.</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Increasing our incomes by changing employers. Maxing out any and all retirement savings accounts that we could. Eliminating all debt other than our mortgage. Selling our sexy vehicles for older but dependable cars.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>Started saving more so much sooner! Not fall into lifestyle creep and try to match what others were doing to achieve a so called status.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Try different methods to find out what works best for you. If you hate debt eliminate it. Earn as much as possible, without sacrificing your health and time with your family. Save just a bit more than you think you should every month. Automate saving and investing. Don't give up. If you need to modify your plan, that's okay. Forgive yourself for any mistakes, make corrections as needed, and keep on going.</p>
