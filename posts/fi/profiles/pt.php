  <h3>Philip Taylor from <a href="https://ptmoney.com" target="_blank">PT Money</a></h3>
  <p class="lead">41m, married, working and saving money in Frisco, TX. $709,500 in savings. Spending $80k/yr now, $50k in retirement. FI by 47 in a super expensive city. RE... What is this 'retire' that you speak of?</p>
  <p>
    Married, father of three, former practicing CPA turned full-time blogger, podcaster, and conference/event planner.
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I first learned about FI when I read David Bach's Automatic Millionaire. He showed me what was possible. I further developed my understanding of it in the mid 2000s when reading personal finance blogs like Consumerism Commentary and Blueprint for Financial Prosperity. Then I found Jacob's ERE and it changed my whole understanding of what was possible.</p>

    <h4>What was your financial status when you began?</h4>
    <p>When I first began my wife and I had some college debt (roughly 30k), car loans (around 25k), and little savings ($40k).</p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Automating my savings and maxing out my retirement accounts each year has had the biggest impact on my finances; along with starting my own businesses which have fast tracked my wealth and given me the feeling like I'm no longer working.</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>I'm pretty happy with how it's all been laid out. But it would have been nice to kick start things a bit sooner. I stayed too long in Corporate America - 10 years.</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Find the life you want to live first - then save aggressively for it. The "why" is so much more important and interesting than the how.</p>
