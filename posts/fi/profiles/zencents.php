  <h3>Zed from <a href="http://zencents.com" target="_blank">zencents</a></h3>
  <p class="lead">32m, married and saving in Boston, MA. $150k in savings. Spending <a href="http://www.zencents.com/monthly-expense-reports/" target="_blank">varies</a>, $20k/yr in retirement. FI at 40, RE at 45 in a rural area.</p>
  <p>
    Engineer by day. Financial engineer by night! Suburban dwelling, home owning millennial looking to take back my life, by taking back my time. Love the four seasons, love to be outdoors, love my wife and love my puppy! (She's my puppy, even if she is approaching 7 years!)
  </p>

  <div class="profile-toggle--more">
    <h4>How did you learn about FI?</h4>
    <p>I first came across Financial Independence in early 2015 when I fell into the Mr. Money Mustache wormhole. When I came out the other end I realized I needed to strive for FI myself. </p>

    <h4>What was your financial status when you began?</h4>
    <p>Back then I was a silly-spender. I purchased too many little "trinkets" and wasted my income, mostly because it didn't have a better purpose. Strangely enough, I  had "ONLY $30,000" left in student loan debt. Coming from over $100k in student loan debt, that $30k seemed small. Someone should have slapped me upside the head and told me to use my extra cash on that! </p>

    <h4>What has been the most impactful change you've made towards FI?</h4>
    <p>Focused on paying debt! My debt was such a hindrance to working towards any financial goal. It was always there, never shrinking, and basically just a monkey on my back. Finally realizing that FI is possible, made me rethink my lifestyle to work on destroying my debt. It didn't take long after that to finally wipe out the final $30,000!</p>

    <h4>What, if anything, do you wish you could have done differently?</h4>
    <p>Oh boy! I wish I didn't inflate my lifestyle as soon as I got that first engineering paycheck. I "saved" in my 401k. I "saved" for an emergency fund. Looking back, I barely accumulated anything because paying myself was not my first priority. Save early. Save often!</p>

    <h4>What advice do you have for others going down the path of FI?</h4>
    <p>Realize that FI is primarily a waiting game. And all that time waiting to reach FI, that is what they call YOUR LIFE! So, don't get too focused on saving and racing towards Financial Independence that you let your life slip by. Get out there, explore, and discover your true passions! Life is better when you're having fun!</p>
